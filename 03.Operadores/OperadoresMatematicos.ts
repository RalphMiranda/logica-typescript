let primeiroNumero :number = 10;
let segundoNumero :number = 4;

let soma :number = primeiroNumero + segundoNumero;
let subt :number = primeiroNumero - segundoNumero;
let mult :number = primeiroNumero * segundoNumero;
let divi :number = primeiroNumero / segundoNumero;
let modo :number = primeiroNumero % segundoNumero;

console.log(soma);  // 10
console.log(subt);  // 6
console.log(mult);  // 40
console.log(divi);  // 2.5
console.log(modo);  // 2